export class Pais {

    id:number;
    nombre:string;
    bandera:string;
    capital:string;
    poblacion:number;

    constructor(id:number,nombre:string,bandera:string,capital:string,poblacion:number){
        this.id=id;
        this.nombre=nombre;
        this.bandera=bandera;
        this.capital=capital;
        this.poblacion=poblacion;
    }

}
